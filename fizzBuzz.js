var output = []; // Декларираме глобална променлива (масив), която ще съдържа резултатите
var count = 1;   // Декларираме брояч, който ще се увеличава при всяко извикване на функцията

function fizzBuzz() {
    // Тук добавяме стойности към масива 'output'
    if (count % 3 === 0 && count % 5 === 0) {
        // Ако числото се дели на 3 и на 5, добавяме "FizzBuzz" към масива
        output.push("FizzBuzz");
    } 
    else if (count % 3 === 0) {
        // Ако числото се дели само на 3, добавяме "Fizz"
        output.push("Fizz");
    } 
    else if (count % 5 === 0) {
        // Ако числото се дели само на 5, добавяме "Buzz"
        output.push("Buzz");
    } 
    else {
        // Ако числото не се дели нито на 3, нито на 5, добавяме самото число
        output.push(count);
    }

    count++; // Увеличаваме стойността на брояча за следващото извикване
    console.log(output); // Отпечатваме текущото съдържание на масива 'output' в конзолата
}